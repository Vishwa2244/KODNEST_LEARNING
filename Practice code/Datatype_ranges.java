package Java_Program;

public class Datatype_ranges {

	public static void main(String[] args) {
		System.out.println("Int Max range is "+Integer.MAX_VALUE);    //  Int Max range--->2147483647
		System.out.println("Int Min range is "+Integer.MIN_VALUE);    // Int Min range--->-2147483648
		System.out.println("double Max range is "+Double.MAX_VALUE);  // double Max range--->1.7976931348623157E308
		System.out.println("double Min range is "+Double.MIN_VALUE);  // double Min range--->4.9E-324
		System.out.println("Float Max range is " +Float.MAX_VALUE);    // Float Max range--->3.4028235E38
		System.out.println("Float Min range is " +Float.MIN_VALUE);    // Float Min range--->1.4E-45
		System.out.println("Long Max range is "+Long.MAX_VALUE);      // Long Max range--->9223372036854775807
		System.out.println("Long Min range is "+Long.MIN_VALUE);      // Long Min range--->-9223372036854775808
		

	}

}
